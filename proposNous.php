    <h2>À propos de nous</h2>
    <p>La maison des jeunes est un carrefour d’information, un espace d’échange, de prise en charge des besoins et
        des projets des jeunes par les jeunes, un lieu de passage et de progression vers l’autonomie.
    <p>Mise sur pied en 1990, la maison des jeunes organise et viens en aide à quelques 15 000 jeunes par année et
        ce partout dans la province de Québec.</p>

    <div class="line"></div>

    <h2>Sa mission</h2>
    <p>Les maisons des jeunes membres du RMJQ sont des associations de jeunes et d’adultes qui se sont donné pour
        mission, sur une base volontaire, dans leur communauté, de tenir un lieu de rencontre animé où les jeunes de
        12 à 17 ans, au contact d’adultes significatifs, pourront devenir des citoyens et des citoyennes critiques,
        actifs et responsables.</p>

    <div class="line"></div>

    <h2>Notre histoire</h2>
    <p>Depuis 1980, le Regroupement des maisons des jeunes du Québec (RMJQ) et ses maisons des jeunes membres ont
        bâti un projet de société qui repose sur la prévention.</p>

